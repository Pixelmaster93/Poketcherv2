CREATE TABLE berries (
    id INT PRIMARY KEY,
    name VARCHAR(25) NOT NULL,
    natural_power INT,
    size INT,
    max_harvest INT,
    growth_time INT,
    soil_dryness INT,
    smoothness INT,
    berries_firmness_id INT,
    item_id INT,
    natural_type_id INT,
    FOREIGN KEY (berries_firmness_id) REFERENCES berries_firmness(id),
    FOREIGN KEY (item_id) REFERENCES items(id),
    FOREIGN KEY (natural_type_id) REFERENCES types(id)
);


